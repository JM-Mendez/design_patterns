package A00_FizzBuzz.java.startingpoint;

public class App {

  private static String toMessage(int i){
    return String.valueOf(i);
  }

  public static void main(String[] args){
    System.out.println(toMessage(0));
    System.out.println(toMessage(1));
    System.out.println(toMessage(2));
    System.out.println(toMessage(3));
    System.out.println(toMessage(4));

    System.out.println("\n\nDone!");
  }
}
